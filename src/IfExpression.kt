fun main() {
    // condititon true
    val nilai = 90;
    if(nilai > 75) {
        println("Good Job")
    }

    // if and else expression
    val example = 60;
    if(example > 75) {
        println("Good Job")
    } else {
        println("Ulangi")
    }

    // else if expression
    val donation = 80;
    if(donation > 80) {
        println("Orang Baik")
    } else if(donation > 70) {
        println("Lumayan Baik")
    } else {
        println("Orang Pelit")
    }

}